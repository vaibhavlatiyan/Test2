
public class IntegerConditions {
	
	public static void main (String []args) {
		int n = 16;
		if (n%2 != 0){
			System.out.println("Weird");
		}
		else {
			if (2<=n && n<=5) {
				System.out.println("Not Weird");
				
				}
			if (6<=n && n<=20) {
				System.out.println("Weird");
		}
			else {
				System.out.println("Not Weird");
			}
		
	}

}
}
